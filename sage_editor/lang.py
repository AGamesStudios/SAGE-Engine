import locale


def get_default_language() -> str:
    """Return the language code matching the system locale."""
    lang, _ = locale.getdefaultlocale()
    if lang and lang.lower().startswith('ru'):
        return 'Русский'
    return 'English'


DEFAULT_LANGUAGE = get_default_language()
